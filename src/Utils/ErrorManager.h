static inline void LogMsg(int level, const char* context, const char* msg)
{
    TraceLog(level, "[%s] %s", context, msg);
}

static inline bool AssertNotNull(const void* ptr, const char* context, const char* varName)
{
    if (!ptr)
    {
        TraceLog(LOG_ERROR, "[%s] %s is NULL", context, varName);
        return false;
    }
    return true;
}
